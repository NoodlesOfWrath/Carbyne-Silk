int main()
{
    printf("If you can keep your head when all about you\n");

    printf("   Are losing theirs and blaming it on you;\n");

    printf("If you can trust yourself when all men doubt you,\n");

    printf("   But make allowance for their doubting too;\n");

    printf("If you can wait and not be tired by waiting,\n");

    printf("   Or, being lied about, don’t deal in lies,\n");

    printf("Or, being hated, don’t give way to hating,\n");

    printf("   And yet don’t look too good, nor talk too wise;\n");

    printf("\n");

    printf("If you can dream—and not make dreams your master;\n");

    printf("   If you can think—and not make thoughts your aim;\n");

    printf("If you can meet with triumph and disaster\n");

    printf("   And treat those two impostors just the same;\n");

    printf("If you can bear to hear the truth you’ve spoken\n");

    printf("   Twisted by knaves to make a trap for fools,\n");

    printf("Or watch the things you gave your life to broken,\n");

    printf("   And stoop and build ’em up with wornout tools;\n");

    printf("\n");

    printf("If you can make one heap of all your winnings\n");

    printf("   And risk it on one turn of pitch-and-toss,\n");

    printf("And lose, and start again at your beginnings\n");

    printf("   And never breathe a word about your loss;\n");

    printf("If you can force your heart and nerve and sinew\n");

    printf("   To serve your turn long after they are gone,\n");

    printf("And so hold on when there is nothing in you\n");

    printf("   Except the Will which says to them: “Hold on”;\n");

    printf("\n");

    printf("If you can talk with crowds and keep your virtue,\n");

    printf("   Or walk with kings—nor lose the common touch;\n");

    printf("If neither foes nor loving friends can hurt you;\n");

    printf("   If all men count with you, but none too much;\n");

    printf("If you can fill the unforgiving minute\n");

    printf("With sixty seconds’ worth of distance run—\n");

    printf("   Yours is the Earth and everything that’s in it,\n");

    printf("And—which is more—you’ll be a Man, my son!\n");

    printf("%d\n", 58);

    return 0;
}